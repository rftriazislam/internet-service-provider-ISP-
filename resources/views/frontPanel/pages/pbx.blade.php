            <h3>Private Branch Exchange (PBX)</h3>
<p>A PBX (private branch exchange) is a telephone system within an enterprise that switches calls between enterprise users on local lines while allowing all users to share a certain number of external phone lines. The main purpose of a PBX is to save the cost of requiring a line for each user to the telephone company’s central office.</p>
<p>The PBX is owned and operated by the enterprise rather than the telephone company (which may be a supplier or service provider, however). Private branch exchanges used analog technology originally. Today, PBXs use digital technology (digital signals are converted to analog for outside calls on the local loop using plain old telephone service (POTS ).</p>
<p>A PBX includes:</p>
<br>
<div class="container">
<ul >
<li >1. Telephone trunk (multiple phone) lines that terminate at the PBX</li>
<li >2. A computer with memory that manages the switching of the calls within the PBX and in and out of it</li>
<li >3. The network of lines within the PBX</li>
<li >4. A console or switchboard for a human operator (optional)</li>
</ul>
</div>
<br>
<br>
<p>In some situations, alternatives to a PBX include centrex service (in which a pool of lines are rented at the phone company’s central office), <i>key telephone systems</i>, and, for very small enterprises, primary rate Integrated Services Digital Network (ISDN).</p>


 <h4>Key IP PBX Features</h4>


<p>If you’re looking for a PBX, here are some of the features you should be sure are included:</p>
<br>
<div class="container">
<ul>
<li>1. <b>VoIP Ready:</b> The world is moving away from legacy PSTN lines and towards VoIP. Make sure your PBX can support IP stations (phones) and IP trunks (service). SIP is the current de facto standard, so don’t buy a phone system that doesn’t support it.</li>
<li>2. <b>Voice Messaging:</b> Once upon a time, voicemail was an optional add-on. Today, it’s table stakes. Look for PBXs that can forward voicemail messages to your email as attachments. If possible, look for IP phones that support visual voicemail.</li>
<li>3. <b> Mobility: </b>Most businesses have at least some road warriors who spend much of their time out of the office. Make sure your PBX supports mobility features like Find Me / Follow Me, remote IP extensions and fixed / mobile convergence.</li>
<li>4. <b> Conferencing:</b> One of the best ways to cut down on travel costs is teleconferencing. Make sure your phone system has native support for true multi-party conferences (not just basic three-way calling).</li>
<li>5. <b> Reporting: </b>If you can’t measure it, you can’t manage it. Make sure that the PBX you pick includes basic call history reporting features.</li>
</ul>

