            <hr>
            <p>An <strong>IP PBX</strong> is a private branch exchange (telephone switching system within an enterprise) that switches calls between VoIP (voice over Internet Protocol or IP) users on local lines while allowing all users to share a certain number of external phone lines. The typical IP PBX can also switch calls between a VoIP user and a traditional telephone user, or between two traditional telephone users in the same way that a conventional PBX does. The abbreviation may appear in various texts as IP-PBX, IP/PBX, or IPPBX.</p>
<p>With a conventional PBX, separate networks are necessary for voice and data communications. One of the main advantages of an IP PBX is the fact that it employs converged data and voice networks. This means that Internet access, as well as VoIP communications and traditional telephone communications, are all possible using a single line to each user. This provides flexibility as an enterprise grows, and can also reduce long-term operation and maintenance costs. Like a traditional PBX, an IP PBX is owned by the enterprise.</p>
      
